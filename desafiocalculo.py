from math import pow

number1 = float(input('Informe o primeiro número: '))
number2 = float(input('Informe o segundo número: '))

print(f'Soma: {number1 + number2}')
print(f'Subtração: {number1 - number2}')
print(f'Multiplicação: {number1 * number2}')
print(f'Divisão: {number1 / number2}')
print(f'Exponenciação: {pow(number1, number2)}')
